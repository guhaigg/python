"""
    拷贝:复制、备份,目的就是互不影响.
    浅拷贝:拷贝第一层
        优点：占用内存较小
        缺点：深层数据共享,互相影响
        适用性：没有深层数据或不被修改
    深拷贝:拷贝所有层
        优点：绝对互不影响
        缺点：占用内存较大
        适用性：优先使用浅拷贝,深层数据会被修改
"""
# 浅拷贝
data01 = ["陈伊南", ["陈志斌", "李朋冲"], "孙子凌"]
data02 = data01[:]
data01[0] = "南南"
data01[1][0] = "陈陈"
print(data02)

# 深拷贝
import copy

data01 = ["陈伊南", ["陈志斌", "李朋冲"], "孙子凌"]
data02 = copy.deepcopy(data01)
data01[0] = "南南"
data01[1][0] = "陈陈"
print(data02)

