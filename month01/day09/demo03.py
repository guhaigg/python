"""
    函数参数
        形式参数
            不定长形参：实参数量无限
                优点：调用者方便(不用自行构建容器)
                星号元组形参
                双号字典形参
"""
# 星号元组形参：将多个位置实参合并为一个元组
# 价值：位置实参数量无限
def func01(*p1):
    print(p1)
# 双号字典形参：将多个关键字实参合并为一个字典
# 价值：关键字实参数量无限
def func02(**p1):
    print(p1)
func01()
func01(10)
func01(10,20)
# func01(p1 = 1) # 不支持关键字实参

func02(a=1,b=2)# 只支持关键字实参
# func02(1,2)