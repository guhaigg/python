"""
    函数参数
        实际参数
            序列实参
            字典实参
            共同作用：将容器的元素传入函数
"""


def func01(p1, p2, p3):
    print(p1)
    print(p2)
    print(p3)


data01 = [1, 2, 3]
data02 = (1, 2, 3)
data03 = "孙悟空"
# 序列实参：将一个序列拆分为多个实参,按顺序与形参对应
func01(*data01)  # 拆
data04 = {"p2": 2, "p1": 1, "p3": 3}
# 字典实参：将一个字典拆分为多个实参,按名字与形参对应
func01(**data04)


def func02(*args):  # 合
    print(args)


# 作用：将容器的元素传入函数
list01 = [1, 2, 3, 4]  # 容器构建过程是未知的
func02(*list01)  # 拆
