"""
    字面值(各种写法)
    int
"""
# 1. 十进制：每位用十种状态计数，逢十进一，写法是0~9
# 9  10  11
number01 = 10

# 2. 二进制BIN：每位用二种状态计数，逢二进一，写法是0~1
# 0   1   10   11   100  101  110
number02 = 0b10

# 3. 八进制OCT：每位用八种状态计数，逢八进一，写法是0~7
# 0 1 2 .. 7  10  11   12
number03 = 0o10

# 4. 十六进制HEX：每位用十六种状态计数，逢十六进一， 写法是0~9  a(10)~f(15)
# 0 1 2 .. 7  10  11   12
number04 = 0x10

